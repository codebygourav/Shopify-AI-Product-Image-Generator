import { json } from "@remix-run/node";
import prisma from "../db.server";
import { authenticate, unauthenticated } from "../shopify.server";
import { generateAiImage } from "../services/openai-images.server";
import { moderatePrompt } from "../services/moderation.server";
import { getOrCreateCustomer, getOrCreateShop } from "../services/shops.server";
import { attachImageToProduct } from "../services/shopify-media.server";

export async function action({ request }) {
  try {
    const body = await request.json();
    const {
      prompt,
      productId,
      productHandle,
      customerId,
      customerEmail,
      visibility = "PRIVATE",
      shop: shopFromBody,
    } = body;

    if (!prompt || typeof prompt !== "string" || prompt.trim().length < 3) {
      return json({ success: false, error: "Prompt is required." }, { status: 400 });
    }

    let admin = null;
    let shopDomain = shopFromBody;

    try {
      const auth = await authenticate.admin(request);
      admin = auth.admin;
      shopDomain = auth.session.shop;
    } catch {
      if (shopFromBody) {
        const auth = await unauthenticated.admin(shopFromBody);
        admin = auth.admin;
        shopDomain = shopFromBody;
      }
    }

    if (!shopDomain) {
      return json(
        { success: false, error: "Shop domain is required." },
        { status: 401 },
      );
    }

    const shop = await getOrCreateShop(shopDomain);
    const customer = await getOrCreateCustomer({
      shopId: shop.id,
      shopifyCustomerId: customerId,
      email: customerEmail,
    });

    const db = prisma;
    const generation = await db.aiImageGeneration.create({
      data: {
        shopId: shop.id,
        customerId: customer?.id,
        productId,
        productHandle,
        prompt: prompt.trim(),
        visibility: visibility === "PUBLIC" ? "PUBLIC" : "PRIVATE",
        status: "PROCESSING",
        moderationStatus: "PENDING",
      },
    });

    const moderation = await moderatePrompt(prompt);
    if (!moderation.allowed) {
      const blocked = await db.aiImageGeneration.update({
        where: { id: generation.id },
        data: {
          status: "BLOCKED",
          moderationStatus: "REJECTED",
          moderationReason: moderation.reason,
        },
      });

      return json(
        { success: false, error: moderation.reason, generation: blocked },
        { status: 422 },
      );
    }

    const image = await generateAiImage(
      `${prompt.trim()}\n\nInclude a tasteful visible watermark text: "Generated by AI".`,
    );

    let media = null;
    if (productId && admin) {
      media = await attachImageToProduct({
        admin,
        productId,
        imageUrl: image.imageUrl,
        alt: `AI generated image for ${productHandle || productId}`,
      });
    }

    const saved = await db.aiImageGeneration.update({
      where: { id: generation.id },
      data: {
        imageUrl: image.imageUrl,
        shopifyMediaId: media?.id,
        status: "COMPLETED",
        moderationStatus: "APPROVED",
        openAiRequestId: image.requestId,
        metadata: {
          productHandle,
          mediaUpload: media,
        },
      },
    });

    await db.openAiUsageLog.create({
      data: {
        shopId: shop.id,
        generationId: saved.id,
        model: image.model,
        imageCount: 1,
        requestId: image.requestId,
        status: "COMPLETED",
      },
    });

    return json({
      success: true,
      generation: saved,
      image: saved.imageUrl,
      media,
    });
  } catch (error) {
    console.error("Image generation failed", error);
    return json({
      success: false,
      error: error.message,
    }, { status: 500 });
  }
}
