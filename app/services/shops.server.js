import prisma from "../db.server";

export async function getOrCreateShop(shopDomain) {
  const db = prisma;
  return db.shop.upsert({
    where: { shop: shopDomain },
    update: {},
    create: {
      shop: shopDomain,
      settings: JSON.stringify(defaultShopSettings()),
    },
  });
}

export function defaultShopSettings() {
  return {
    defaultVisibility: "PRIVATE",
    watermarkText: "Generated by AI",
    moderationEnabled: true,
    studioProduct: {
      title: "Generate your own image",
      checkoutVariantId: "",
      promptInstructions:
        "Create premium print-ready wall art. Respect the selected product options exactly.",
      optionGroups: [
        {
          name: "Size",
          promptLabel: "print size",
          values: ["8 x 10 in", "12 x 16 in", "18 x 24 in"],
        },
        {
          name: "Frame",
          promptLabel: "frame style",
          values: ["No frame", "Black frame", "Walnut frame", "White frame"],
        },
        {
          name: "Fabric",
          promptLabel: "material",
          values: ["Fine art paper", "Canvas", "Textured cotton"],
        },
      ],
      promptTemplates: [
        "minimal gallery wall, premium print art, soft natural light",
        "bold abstract print, textured brushwork, curated interior styling",
        "calm spiritual artwork, refined color palette, elegant framed print",
      ],
    },
  };
}

export function parseShopSettings(settings) {
  let parsed = {};

  try {
    parsed = settings ? JSON.parse(settings) : {};
  } catch {
    parsed = {};
  }

  const defaults = defaultShopSettings();
  return {
    ...defaults,
    ...parsed,
    studioProduct: {
      ...defaults.studioProduct,
      ...(parsed.studioProduct || {}),
      optionGroups: Array.isArray(parsed.studioProduct?.optionGroups)
        ? parsed.studioProduct.optionGroups
        : defaults.studioProduct.optionGroups,
      promptTemplates: Array.isArray(parsed.studioProduct?.promptTemplates)
        ? parsed.studioProduct.promptTemplates
        : defaults.studioProduct.promptTemplates,
    },
  };
}

export async function getOrCreateCustomer({ shopId, shopifyCustomerId, email }) {
  if (!shopifyCustomerId && !email) return null;

  const db = prisma;
  const existing = await db.customerAccount.findFirst({
    where: {
      shopId,
      OR: [
        shopifyCustomerId ? { shopifyCustomerId } : undefined,
        email ? { email } : undefined,
      ].filter(Boolean),
    },
  });

  if (existing) return existing;

  return db.customerAccount.create({
    data: {
      shopId,
      shopifyCustomerId,
      email,
    },
  });
}
