/**
 * Gets Shop customization settings from standard Shop Metafields.
 */
export async function getShopSettings(admin) {
  const query = `#graphql
    query GetShopSettings {
      shop {
        id
        metafield(namespace: "ai_image_manager", key: "settings") {
          value
        }
      }
    }
  `;

  try {
    const response = await admin.graphql(query);
    const result = await response.json();
    return result.data?.shop?.metafield?.value || null;
  } catch (err) {
    console.error("getShopSettings error", err);
    return null;
  }
}

/**
 * Updates Shop customization settings in standard Shop Metafields.
 */
export async function updateShopSettings(admin, settingsStr) {
  // First, we need to query the shop ID to set the ownerId
  const shopIdQuery = `#graphql
    query GetShopId {
      shop {
        id
      }
    }
  `;

  try {
    const shopIdRes = await admin.graphql(shopIdQuery);
    const shopIdJson = await shopIdRes.json();
    const shopId = shopIdJson.data?.shop?.id;

    if (!shopId) throw new Error("Could not fetch shop ID");

    const mutation = `#graphql
      mutation SetShopSettings($metafields: [MetafieldsSetInput!]!) {
        metafieldsSet(metafields: $metafields) {
          metafields {
            id
            value
          }
          userErrors {
            field
            message
          }
        }
      }
    `;

    const variables = {
      metafields: [
        {
          ownerId: shopId,
          namespace: "ai_image_manager",
          key: "settings",
          type: "json",
          value: settingsStr,
        },
      ],
    };

    const response = await admin.graphql(mutation, { variables });
    const result = await response.json();
    const errors = result.data?.metafieldsSet?.userErrors || [];
    if (errors.length) {
      throw new Error(errors.map((e) => e.message).join(", "));
    }

    return result.data?.metafieldsSet?.metafields?.[0];
  } catch (err) {
    console.error("updateShopSettings error", err);
    throw err;
  }
}

/**
 * Gets a Customer approval profile metafield.
 */
export async function getCustomerProfile(admin, customerId) {
  if (!customerId) return null;

  // Normalize ID (needs to have gid://shopify/Customer/ prefix)
  const fullCustomerId = customerId.startsWith("gid://shopify/")
    ? customerId
    : `gid://shopify/Customer/${customerId}`;

  const query = `#graphql
    query GetCustomerProfile($id: ID!) {
      customer(id: $id) {
        id
        metafield(namespace: "ai_image_manager", key: "profile") {
          value
        }
      }
    }
  `;

  try {
    const response = await admin.graphql(query, {
      variables: { id: fullCustomerId },
    });
    const result = await response.json();
    const rawVal = result.data?.customer?.metafield?.value;
    return rawVal ? parseJson(rawVal) : null;
  } catch (err) {
    console.error("getCustomerProfile error", err);
    return null;
  }
}

/**
 * Updates a Customer approval profile metafield.
 */
export async function updateCustomerProfile(admin, customerId, profileData) {
  if (!customerId) return null;

  const fullCustomerId = customerId.startsWith("gid://shopify/")
    ? customerId
    : `gid://shopify/Customer/${customerId}`;

  // Fetch existing profile to merge updates
  const existing = (await getCustomerProfile(admin, fullCustomerId)) || {};
  const nextProfile = {
    isApproved: true,
    generationLimit: null,
    totalGenerations: 0,
    ...existing,
    ...profileData,
  };

  const mutation = `#graphql
    mutation SetCustomerProfile($metafields: [MetafieldsSetInput!]!) {
      metafieldsSet(metafields: $metafields) {
        metafields {
          id
        }
        userErrors {
          field
          message
        }
      }
    }
  `;

  const variables = {
    metafields: [
      {
        ownerId: fullCustomerId,
        namespace: "ai_image_manager",
        key: "profile",
        type: "json",
        value: JSON.stringify(nextProfile),
      },
    ],
  };

  try {
    const response = await admin.graphql(mutation, { variables });
    const result = await response.json();
    const errors = result.data?.metafieldsSet?.userErrors || [];
    if (errors.length) {
      throw new Error(errors.map((e) => e.message).join(", "));
    }
    return nextProfile;
  } catch (err) {
    console.error("updateCustomerProfile error", err);
    throw err;
  }
}

/**
 * Creates an `ai_image_generation` Metaobject.
 */
export async function createAiImageGeneration(admin, data) {
  try {
    assertPublicImageUrl(data.imageUrl);
    const shop = await ensureDatabaseShop(data.shopId);
    const customer = await ensureDatabaseCustomer({
      shopId: shop.id,
      shopifyCustomerId: data.customerId,
      email: data.customerEmail,
    });

    const generation = await db.aiImageGeneration.create({
      data: {
        shopId: shop.id,
        customerId: customer?.id || null,
        productId: safeDbString(data.productId),
        productHandle: safeDbString(data.productHandle),
        variantId: safeDbString(data.variantId),
        variantTitle: safeDbString(data.variantTitle),
        prompt: safeDbString(data.prompt) || "",
        imageUrl: data.imageUrl || null,
        shopifyMediaId: safeDbString(data.shopifyMediaId),
        status: data.status || "PENDING",
        visibility: data.visibility || "PRIVATE",
        moderationStatus: data.moderationStatus || "PENDING",
        moderationReason: safeDbString(data.moderationReason),
        watermarkText: safeDbString(data.watermarkText) || "Generated by AI",
        openAiRequestId: safeDbString(data.openAiRequestId),
        errorMessage: safeDbString(data.errorMessage),
        metadata: safeDbString(data.metadata) || "{}",
        selectedForCart: Boolean(data.selectedForCart),
      },
      include: { customer: true },
    });

    return mapDatabaseGenerationToGeneration(generation);
  } catch (err) {
    console.error("createAiImageGeneration error", err);
    throw err;
  }
}

/**
 * Updates an `ai_image_generation` Metaobject.
 */
export async function updateAiImageGeneration(admin, id, data) {
  try {
    assertPublicImageUrl(data.imageUrl);
    const updateData = {};

    if (data.prompt !== undefined)
      updateData.prompt = safeDbString(data.prompt);
    if (data.status !== undefined) updateData.status = data.status;
    if (data.visibility !== undefined) updateData.visibility = data.visibility;
    if (data.moderationStatus !== undefined) {
      updateData.moderationStatus = data.moderationStatus;
    }
    if (data.imageUrl !== undefined)
      updateData.imageUrl = data.imageUrl || null;
    if (data.productId !== undefined)
      updateData.productId = safeDbString(data.productId);
    if (data.productHandle !== undefined) {
      updateData.productHandle = safeDbString(data.productHandle);
    }
    if (data.variantId !== undefined)
      updateData.variantId = safeDbString(data.variantId);
    if (data.variantTitle !== undefined) {
      updateData.variantTitle = safeDbString(data.variantTitle);
    }
    if (data.shopifyMediaId !== undefined) {
      updateData.shopifyMediaId = safeDbString(data.shopifyMediaId);
    }
    if (data.moderationReason !== undefined) {
      updateData.moderationReason = safeDbString(data.moderationReason);
    }
    if (data.watermarkText !== undefined) {
      updateData.watermarkText =
        safeDbString(data.watermarkText) || "Generated by AI";
    }
    if (data.openAiRequestId !== undefined) {
      updateData.openAiRequestId = safeDbString(data.openAiRequestId);
    }
    if (data.errorMessage !== undefined) {
      updateData.errorMessage = safeDbString(data.errorMessage);
    }
    if (data.metadata !== undefined)
      updateData.metadata = safeDbString(data.metadata) || "{}";
    if (data.selectedForCart !== undefined) {
      updateData.selectedForCart = Boolean(data.selectedForCart);
    }

    const generation = await db.aiImageGeneration.update({
      where: { id },
      data: updateData,
      include: { customer: true },
    });

    return mapDatabaseGenerationToGeneration(generation);
  } catch (err) {
    console.error("updateAiImageGeneration error", err);
    throw err;
  }
}

