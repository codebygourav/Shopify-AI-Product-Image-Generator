/**
 * Queries AI Image Generations from Shopify Metaobjects.
 * Supports standard filters.
 */
export async function getAiImageGenerations(admin, filter = {}) {
  await ensureMetaobjectDefinition(admin);

  const query = `#graphql
    query GetMetaobjects($type: String!, $first: Int!) {
      metaobjects(type: $type, first: $first) {
        edges {
          node {
            id
            handle
            type
            fields {
              key
              value
            }
          }
        }
      }
    }
  `;

  try {
    const response = await admin.graphql(query, {
      variables: {
        type: "ai_image_generation",
        first: 100, // Fetch up to 100 generations
      }
    });
    const result = await response.json();
    const edges = result.data?.metaobjects?.edges || [];
    
    // Map Metaobjects and apply in-memory filtering for robustness and simple logic
    let list = edges.map(({ node }) => mapMetaobjectToGeneration(node));

    if (filter.customerId) {
      list = list.filter(item => item.customerId === filter.customerId);
    }
    if (filter.customerEmail) {
      list = list.filter(item => item.customerEmail === filter.customerEmail);
    }
    if (filter.productId) {
      list = list.filter(item => item.productId === filter.productId);
    }
    if (filter.visibility) {
      list = list.filter(item => item.visibility === filter.visibility);
    }
    if (filter.moderationStatus) {
      list = list.filter(item => item.moderationStatus === filter.moderationStatus);
    }
    if (filter.status) {
      list = list.filter(item => item.status === filter.status);
    }

    // Sort by createdAt descending
    list.sort((a, b) => new Date(b.createdAt) - new Date(a.createdAt));

    if (filter.take) {
      list = list.slice(0, filter.take);
    }

    return list;
  } catch (err) {
    console.error("getAiImageGenerations error", err);
    return [];
  }
}

/**
 * Queries list of Shopify customers along with their `ai_image_manager.profile` metafields.
 */
export async function getCustomers(admin) {
  const query = `#graphql
    query GetCustomers {
      customers(first: 100) {
        edges {
          node {
            id
            displayName
            email
            metafield(namespace: "ai_image_manager", key: "profile") {
              value
            }
          }
        }
      }
    }
  `;

  try {
    const response = await admin.graphql(query);
    const result = await response.json();
    const edges = result.data?.customers?.edges || [];

    const generations = await getAiImageGenerations(admin);

    return edges.map(({ node }) => {
      const profile = node.metafield?.value ? parseJson(node.metafield.value) : {};
      
      // Calculate active generation count for this specific customer
      const count = generations.filter(gen => gen.customerId === node.id).length;

      return {
        id: node.id,
        shopifyCustomerId: node.id,
        displayName: node.displayName || node.email || "Shopify Customer",
        email: node.email,
        isApproved: profile.isApproved !== false, // default true
        generationLimit: profile.generationLimit ?? null,
        _count: {
          generations: count
        }
      };
    });
  } catch (err) {
    console.error("getCustomers error", err);
    return [];
  }
}
