/**
 * Queries AI Image Generations from Shopify Metaobjects.
 * Supports standard filters.
 */
export async function getAiImageGenerations(admin, filter = {}) {
  try {
    const where = {};
    let dbShopId = null;
    if (filter.shopId) {
      const shop = await db.shop.findUnique({ where: { shop: filter.shopId } });
      dbShopId = shop?.id || "__missing_shop__";
      where.shopId = dbShopId;
    }
    if (filter.customerId) {
      if (dbShopId) {
        const customer = await db.customerAccount.findFirst({
          where: {
            shopId: dbShopId,
            shopifyCustomerId: {
              in: customerIdVariants(filter.customerId),
            },
          },
        });
        where.customerId = customer?.id || "__missing_customer__";
      } else {
        where.customerId = filter.customerId;
      }
    }
    if (filter.productId) where.productId = filter.productId;
    if (filter.visibility) where.visibility = filter.visibility;
    if (filter.moderationStatus) {
      where.moderationStatus = filter.moderationStatus;
    }
    if (filter.status) where.status = filter.status;

    const generations = await db.aiImageGeneration.findMany({
      where,
      include: { customer: true },
      orderBy: { createdAt: "desc" },
      take: filter.take || 100,
    });

    let list = generations.map(mapDatabaseGenerationToGeneration);
    if (filter.customerEmail) {
      list = list.filter((item) => item.customerEmail === filter.customerEmail);
    }
    return list;
  } catch (err) {
    console.error("getAiImageGenerations error", err);
    return [];
  }
}

/**
 * Queries list of Shopify customers along with their `ai_image_manager.profile` metafields.
 */
export async function getCustomers(admin) {
  const query = `#graphql
    query GetCustomers {
      customers(first: 100) {
        edges {
          node {
            id
            displayName
            email
            metafield(namespace: "ai_image_manager", key: "profile") {
              value
            }
          }
        }
      }
    }
  `;

  try {
    const response = await admin.graphql(query);
    const result = await response.json();
    const edges = result.data?.customers?.edges || [];

    const generations = await getAiImageGenerations(admin);

    return edges.map(({ node }) => {
      const profile = node.metafield?.value
        ? parseJson(node.metafield.value)
        : {};

      // Calculate active generation count for this specific customer
      const customerIds = customerIdVariants(node.id);
      const count = generations.filter(
        (gen) =>
          customerIds.includes(gen.customerId) ||
          customerIds.includes(gen.customer?.shopifyCustomerId),
      ).length;

      return {
        id: node.id,
        shopifyCustomerId: node.id,
        displayName: node.displayName || node.email || "Shopify Customer",
        email: node.email,
        isApproved: profile.isApproved !== false, // default true
        generationLimit: profile.generationLimit ?? null,
        _count: {
          generations: count,
        },
      };
    });
  } catch (err) {
    console.error("getCustomers error", err);
    return [];
  }
}
