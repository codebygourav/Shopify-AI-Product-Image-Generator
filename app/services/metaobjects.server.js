/**
 * Service to manage application data natively inside Shopify using Metaobjects and Metafields.
 * This completely replaces the need for an external Prisma-managed database.
 */

// A simple utility to parse JSON safely
function parseJson(str, fallback = {}) {
  try {
    return str ? JSON.parse(str) : fallback;
  } catch {
    return fallback;
  }
}

/**
 * Maps a Shopify Metaobject record back to the Prisma-like image generation structure.
 */
export function mapMetaobjectToGeneration(metaobject) {
  if (!metaobject) return null;

  const fields = {};
  if (Array.isArray(metaobject.fields)) {
    for (const field of metaobject.fields) {
      fields[field.key] = field.value;
    }
  }

  // Virtualize the customer object to match Prisma includes
  const customer = fields.customer_id ? {
    id: fields.customer_id,
    shopifyCustomerId: fields.customer_id,
    email: fields.customer_email || null,
    displayName: fields.customer_email ? fields.customer_email.split("@")[0] : "Customer",
  } : null;

  return {
    id: metaobject.id,
    handle: metaobject.handle,
    prompt: fields.prompt || "",
    status: fields.status || "PENDING",
    visibility: fields.visibility || "PRIVATE",
    moderationStatus: fields.moderation_status || "PENDING",
    imageUrl: fields.image_url || null,
    productId: fields.product_id || null,
    productHandle: fields.product_handle || null,
    variantId: fields.variant_id || null,
    variantTitle: fields.variant_title || null,
    shopifyMediaId: fields.shopify_media_id || null,
    moderationReason: fields.moderation_reason || null,
    watermarkText: fields.watermark_text || "Generated by AI",
    openAiRequestId: fields.openai_request_id || null,
    errorMessage: fields.error_message || null,
    metadata: fields.metadata || "{}",
    customerId: fields.customer_id || null,
    customerEmail: fields.customer_email || null,
    createdAt: fields.created_at || new Date().toISOString(),
    updatedAt: fields.created_at || new Date().toISOString(),
    customer,
  };
}

/**
 * Verifies that the `ai_image_generation` Metaobject definition exists.
 * If it doesn't, automatically creates it in the merchant's store.
 */
export async function ensureMetaobjectDefinition(admin) {
  try {
    // 1. Check if definition already exists
    const checkQuery = `#graphql
      query GetMetaobjectDefinition {
        metaobjectDefinitionByType(type: "ai_image_generation") {
          id
          type
          name
        }
      }
    `;

    const checkResponse = await admin.graphql(checkQuery);
    const checkResult = await checkResponse.json();

    if (checkResult.data?.metaobjectDefinitionByType) {
      return checkResult.data.metaobjectDefinitionByType;
    }

    // 2. Define the schema if it doesn't exist
    const createMutation = `#graphql
      mutation CreateMetaobjectDefinition($definition: MetaobjectDefinitionCreateInput!) {
        metaobjectDefinitionCreate(definition: $definition) {
          metaobjectDefinition {
            id
            type
          }
          userErrors {
            field
            message
          }
        }
      }
    `;

    const variables = {
      definition: {
        name: "AI Image Generation",
        type: "ai_image_generation",
        fieldDefinitions: [
          { name: "Prompt", key: "prompt", type: "multi_line_text_field" },
          { name: "Status", key: "status", type: "single_line_text_field" },
          { name: "Visibility", key: "visibility", type: "single_line_text_field" },
          { name: "Moderation Status", key: "moderation_status", type: "single_line_text_field" },
          { name: "Image URL", key: "image_url", type: "single_line_text_field" },
          { name: "Product ID", key: "product_id", type: "single_line_text_field" },
          { name: "Product Handle", key: "product_handle", type: "single_line_text_field" },
          { name: "Variant ID", key: "variant_id", type: "single_line_text_field" },
          { name: "Variant Title", key: "variant_title", type: "single_line_text_field" },
          { name: "Shopify Media ID", key: "shopify_media_id", type: "single_line_text_field" },
          { name: "Moderation Reason", key: "moderation_reason", type: "single_line_text_field" },
          { name: "Watermark Text", key: "watermark_text", type: "single_line_text_field" },
          { name: "OpenAI Request ID", key: "openai_request_id", type: "single_line_text_field" },
          { name: "Error Message", key: "error_message", type: "single_line_text_field" },
          { name: "Metadata", key: "metadata", type: "json" },
          { name: "Customer ID", key: "customer_id", type: "single_line_text_field" },
          { name: "Customer Email", key: "customer_email", type: "single_line_text_field" },
          { name: "Created At", key: "created_at", type: "single_line_text_field" }
        ]
      }
    };

    const createResponse = await admin.graphql(createMutation, { variables });
    const createResult = await createResponse.json();

    const errors = createResult.data?.metaobjectDefinitionCreate?.userErrors || [];
    if (errors.length) {
      console.error("Failed to create metaobject definition:", errors);
      throw new Error(errors.map(e => e.message).join(", "));
    }

    return createResult.data?.metaobjectDefinitionCreate?.metaobjectDefinition;
  } catch (err) {
    console.error("ensureMetaobjectDefinition error", err);
    return null;
  }
}

