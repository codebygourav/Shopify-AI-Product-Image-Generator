/**
 * Service to manage application data natively inside Shopify using Metaobjects and Metafields.
 * This completely replaces the need for an external Prisma-managed database.
 */
import db from "../db.server";

// A simple utility to parse JSON safely
function parseJson(str, fallback = {}) {
  try {
    return str ? JSON.parse(str) : fallback;
  } catch {
    return fallback;
  }
}

/**
 * Maps a Shopify Metaobject record back to the Prisma-like image generation structure.
 */
export function mapMetaobjectToGeneration(metaobject) {
  if (!metaobject) return null;

  const fields = {};
  if (Array.isArray(metaobject.fields)) {
    for (const field of metaobject.fields) {
      fields[field.key] = field.value;
    }
  }

  // Virtualize the customer object to match Prisma includes
  const customer = fields.customer_id
    ? {
        id: fields.customer_id,
        shopifyCustomerId: fields.customer_id,
        email: fields.customer_email || null,
        displayName: fields.customer_email
          ? fields.customer_email.split("@")[0]
          : "Customer",
      }
    : null;

  return {
    id: metaobject.id,
    handle: metaobject.handle,
    prompt: fields.prompt || "",
    status: fields.status || "PENDING",
    visibility: fields.visibility || "PRIVATE",
    moderationStatus: fields.moderation_status || "PENDING",
    imageUrl: fields.image_url || null,
    productId: fields.product_id || null,
    productHandle: fields.product_handle || null,
    variantId: fields.variant_id || null,
    variantTitle: fields.variant_title || null,
    shopifyMediaId: fields.shopify_media_id || null,
    moderationReason: fields.moderation_reason || null,
    watermarkText: fields.watermark_text || "Generated by AI",
    openAiRequestId: fields.openai_request_id || null,
    errorMessage: fields.error_message || null,
    metadata: fields.metadata || "{}",
    customerId: fields.customer_id || null,
    customerEmail: fields.customer_email || null,
    createdAt: fields.created_at || new Date().toISOString(),
    updatedAt: fields.created_at || new Date().toISOString(),
    customer,
  };
}

function mapDatabaseGenerationToGeneration(generation) {
  if (!generation) return null;

  const customer = generation.customer
    ? {
        id: generation.customer.id,
        shopifyCustomerId: generation.customer.shopifyCustomerId,
        email: generation.customer.email || null,
        displayName:
          generation.customer.displayName ||
          generation.customer.email?.split("@")[0] ||
          "Customer",
      }
    : null;

  return {
    id: generation.id,
    handle: generation.id,
    prompt: generation.prompt || "",
    status: generation.status || "PENDING",
    visibility: generation.visibility || "PRIVATE",
    moderationStatus: generation.moderationStatus || "PENDING",
    imageUrl: generation.imageUrl || null,
    productId: generation.productId || null,
    productHandle: generation.productHandle || null,
    variantId: generation.variantId || null,
    variantTitle: generation.variantTitle || null,
    shopifyMediaId: generation.shopifyMediaId || null,
    moderationReason: generation.moderationReason || null,
    watermarkText: generation.watermarkText || "Generated by AI",
    openAiRequestId: generation.openAiRequestId || null,
    errorMessage: generation.errorMessage || null,
    metadata: generation.metadata || "{}",
    customerId: customer?.shopifyCustomerId || generation.customerId || null,
    customerEmail: customer?.email || null,
    selectedForCart: generation.selectedForCart || false,
    createdAt:
      generation.createdAt instanceof Date
        ? generation.createdAt.toISOString()
        : generation.createdAt,
    updatedAt:
      generation.updatedAt instanceof Date
        ? generation.updatedAt.toISOString()
        : generation.updatedAt,
    customer,
  };
}

async function ensureDatabaseShop(shopDomain) {
  if (!shopDomain) {
    throw new Error("shopId is required to save image generations.");
  }

  return db.shop.upsert({
    where: { shop: shopDomain },
    update: {},
    create: {
      shop: shopDomain,
      settings: "{}",
    },
  });
}

async function ensureDatabaseCustomer({ shopId, shopifyCustomerId, email }) {
  if (!shopifyCustomerId) return null;

  const existing = await db.customerAccount.findFirst({
    where: {
      shopId,
      shopifyCustomerId,
    },
  });

  if (existing) {
    return db.customerAccount.update({
      where: { id: existing.id },
      data: {
        email: email || existing.email,
        displayName: email?.split("@")[0] || existing.displayName || "Customer",
      },
    });
  }

  return db.customerAccount.create({
    data: {
      shopId,
      shopifyCustomerId,
      email: email || null,
      displayName: email ? email.split("@")[0] : "Customer",
    },
  });
}

function assertPublicImageUrl(imageUrl) {
  if (!imageUrl) return;
  if (String(imageUrl).startsWith("data:") || String(imageUrl).length > 2048) {
    throw new Error(
      "Generated image must be uploaded to a public URL before it is saved.",
    );
  }
}

function safeDbString(value, maxLength = 180) {
  if (value === undefined || value === null) return null;
  const stringValue = typeof value === "string" ? value : JSON.stringify(value);
  return stringValue.length > maxLength
    ? stringValue.slice(0, maxLength)
    : stringValue;
}

function customerIdVariants(customerId) {
  if (!customerId) return [];

  const raw = String(customerId);
  const numeric = raw.match(/Customer\/([^/]+)$/)?.[1] || raw;
  return Array.from(
    new Set([raw, numeric, `gid://shopify/Customer/${numeric}`]),
  );
}

