(function () {
  function apiUrl(base, path) {
    return `${String(base || "").replace(/\/$/, "")}${path}`;
  }

  document.querySelectorAll("[data-ai-image-generator]").forEach((root) => {
    const button = root.querySelector("[data-ai-generate]");
    const textarea = root.querySelector("textarea");
    const status = root.querySelector("[data-ai-status]");
    const preview = root.querySelector("[data-ai-preview]");
    const publicInput = root.querySelector("[data-ai-public]");

    button.addEventListener("click", async () => {
      const prompt = textarea.value.trim();
      if (!prompt) {
        status.textContent = "Enter a prompt first.";
        return;
      }

      button.disabled = true;
      status.textContent = "Checking prompt and generating image...";
      preview.hidden = true;

      try {
        const response = await fetch(apiUrl(root.dataset.apiBase, "/api/generate-image"), {
          method: "POST",
          headers: { "Content-Type": "application/json" },
          body: JSON.stringify({
            shop: root.dataset.shop,
            productId: root.dataset.productId,
            productHandle: root.dataset.productHandle,
            customerId: root.dataset.customerId,
            customerEmail: root.dataset.customerEmail,
            prompt,
            visibility: publicInput.checked ? "PUBLIC" : "PRIVATE",
          }),
        });
        const data = await response.json();
        if (!data.success) throw new Error(data.error || "Image generation failed.");

        preview.innerHTML = `<img src="${data.image}" alt="${escapeHtml(prompt)}"><span>Generated by AI</span>`;
        preview.hidden = false;
        status.textContent = data.media?.id
          ? "Generated and saved to product media."
          : "Generated. Product media upload will run after CDN storage is configured.";
      } catch (error) {
        status.textContent = error.message;
      } finally {
        button.disabled = false;
      }
    });
  });

  document.querySelectorAll("[data-ai-gallery]").forEach(async (root) => {
    const grid = root.querySelector("[data-ai-gallery-grid]");
    try {
      const response = await fetch(apiUrl(root.dataset.apiBase, `/api/gallery?shop=${encodeURIComponent(root.dataset.shop)}`));
      const data = await response.json();
      grid.innerHTML = data.images
        .map((image) => `
          <article class="ai-image-gallery__item">
            <img src="${image.imageUrl}" alt="${escapeHtml(image.prompt)}">
            <p>${escapeHtml(image.prompt)}</p>
            <small>${image._count.likes} likes · ${image._count.reviews} reviews · ${image._count.comments} comments</small>
          </article>
        `)
        .join("");
    } catch (error) {
      grid.innerHTML = `<p>${escapeHtml(error.message)}</p>`;
    }
  });

  function escapeHtml(value) {
    return String(value || "")
      .replace(/&/g, "&amp;")
      .replace(/</g, "&lt;")
      .replace(/>/g, "&gt;")
      .replace(/"/g, "&quot;")
      .replace(/'/g, "&#039;");
  }
})();
